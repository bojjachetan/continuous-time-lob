# Validation split
# ---------------------------------------------------------------------

def chronological_train_val_split(
    dataset: Dataset,
    val_fraction: float = 0.2,
    min_val_samples: int = 32,
    purge_us: int = 10_000_000,
) -> Tuple[Subset, Subset]:
    n = len(dataset)
    if n < 2:
        raise ValueError("Need at least 2 samples for train/val split.")
    if not hasattr(dataset, "t_us"):
        raise TypeError("Dataset must expose a chronological t_us array for purged validation.")

    val_n = max(min_val_samples, int(round(n * val_fraction)))
    val_n = min(val_n, max(1, n - 1))
    val_start_idx = n - val_n
    t_us = np.asarray(getattr(dataset, "t_us"), dtype=np.int64)
    val_start_t = int(t_us[val_start_idx])
    train_n = int(np.searchsorted(t_us, val_start_t - int(purge_us), side="left"))
    if train_n <= 0:
        raise ValueError("Purged validation left no training samples. Increase fold size or reduce purge_us.")

    train_idx = list(range(0, train_n))
    val_idx = list(range(val_start_idx, n))
    return Subset(dataset, train_idx), Subset(dataset, val_idx)


