def discover_fold_ids(data_dir: str, model_name: str) -> List[int]:
    subdir = Path(data_dir) / model_name
    if not subdir.exists():
        raise FileNotFoundError(f"Missing baseline data subdir: {subdir}")

    fold_ids = set()
    for path in subdir.glob("fold_*_train.npz"):
        stem = path.stem
        # fold_001_train -> 001
        parts = stem.split("_")
        if len(parts) >= 3:
            try:
                fold_ids.add(int(parts[1]))
            except ValueError:
                pass

    if not fold_ids:
        raise ValueError(f"No fold_XXX_train.npz files found under {subdir}")

    return sorted(fold_ids)


def fold_npz_paths(data_dir: str, model_name: str, fold_id: int) -> Tuple[str, str]:
    fold_name = f"fold_{fold_id:03d}"
    train_path = os.path.join(data_dir, model_name, f"{fold_name}_train.npz")
    test_path = os.path.join(data_dir, model_name, f"{fold_name}_test.npz")
    if not os.path.exists(train_path):
        raise FileNotFoundError(train_path)
    if not os.path.exists(test_path):
        raise FileNotFoundError(test_path)
    return train_path, test_path


