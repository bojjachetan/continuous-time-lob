def _load_baselines_module(module_path: str):
    module_path = os.path.abspath(module_path)
    spec = importlib.util.spec_from_file_location("lob_baselines", module_path)
    if spec is None or spec.loader is None:
        raise ImportError(f"Could not load baselines module from {module_path}")
    module = importlib.util.module_from_spec(spec)
    sys.modules[spec.name] = module
    spec.loader.exec_module(module)
    return module


