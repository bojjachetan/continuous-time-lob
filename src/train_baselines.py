def collate_deeplob(batch: List[Dict[str, object]]) -> Dict[str, Tensor]:
    X = torch.stack([item["X"] for item in batch], dim=0)
    y = torch.stack([item["y"] for item in batch], dim=0)
    t_us = torch.tensor([item["t_us"] for item in batch], dtype=torch.long)
    event_id = torch.tensor([item["event_id"] for item in batch], dtype=torch.long)
    return {"X": X, "y": y, "t_us": t_us, "event_id": event_id}


def collate_static_gcn(batch: List[Dict[str, object]]) -> Dict[str, Tensor]:
    node_x = torch.stack([item["node_x"] for item in batch], dim=0)
    y = torch.stack([item["y"] for item in batch], dim=0)
    t_us = torch.tensor([item["t_us"] for item in batch], dtype=torch.long)
    event_id = torch.tensor([item["event_id"] for item in batch], dtype=torch.long)
    return {"node_x": node_x, "y": y, "t_us": t_us, "event_id": event_id}


