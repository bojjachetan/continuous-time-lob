def run_baseline_experiment(cfg: RunnerConfig) -> Dict[str, object]:
    start_time = time.time()
    resolved_device = resolve_device(cfg.device)
    cfg = RunnerConfig(**{**asdict(cfg), "device": resolved_device})
    baselines_module = _load_baselines_module(cfg.baselines_module_path)
    fold_ids = discover_fold_ids(cfg.data_dir, cfg.model_name)

    print(describe_device(resolved_device))
    print(f"Baseline model: {cfg.model_name}")
    print(f"Purged folds: {len(fold_ids)}")

    fold_seed_rows: List[Dict[str, object]] = []
    per_seed_rows: List[Dict[str, object]] = []
    prediction_dir: Optional[str] = None
    if cfg.out_dir is not None:
        os.makedirs(cfg.out_dir, exist_ok=True)
        prediction_dir = os.path.join(cfg.out_dir, "predictions")
        os.makedirs(prediction_dir, exist_ok=True)

    for seed in cfg.seeds:
        seed_fold_metrics: List[Dict[str, float]] = []
        seed_y_true: List[np.ndarray] = []
        seed_y_pred: List[np.ndarray] = []

        for fold_id in fold_ids:
            train_path, test_path = fold_npz_paths(cfg.data_dir, cfg.model_name, fold_id)

            fold_output = train_one_fold(
                cfg=cfg,
                baselines_module=baselines_module,
                train_path=train_path,
                test_path=test_path,
                seed=seed,
            )
            metrics = fold_output["metrics"]

            row = {
                "seed": int(seed),
                "fold_id": int(fold_id),
                **{k: float(v) for k, v in metrics.items()},
            }
            fold_seed_rows.append(row)
            seed_fold_metrics.append(metrics)

            seed_y_true.append(fold_output["predictions"]["y_true"])
            seed_y_pred.append(fold_output["predictions"]["y_pred"])

            if prediction_dir is not None:
                pred_df = pd.DataFrame(
                    {
                        "seed": seed,
                        "fold_id": fold_id,
                        "event_id": fold_output["predictions"]["event_id"],
                        "t_us": fold_output["predictions"]["t_us"],
                        "rv_1s_true": fold_output["predictions"]["y_true"][:, 0],
                        "rv_5s_true": fold_output["predictions"]["y_true"][:, 1],
                        "rv_10s_true": fold_output["predictions"]["y_true"][:, 2],
                        "rv_1s_pred": fold_output["predictions"]["y_pred"][:, 0],
                        "rv_5s_pred": fold_output["predictions"]["y_pred"][:, 1],
                        "rv_10s_pred": fold_output["predictions"]["y_pred"][:, 2],
                    }
                )
                pred_df.to_parquet(
                    os.path.join(
                        prediction_dir,
                        f"{cfg.model_name}_seed_{seed}_fold_{fold_id:03d}.parquet",
                    ),
                    index=False,
                )

        if not seed_y_true:
            raise ValueError("No baseline test predictions were produced.")

        y_true = np.concatenate(seed_y_true, axis=0)
        y_pred = np.concatenate(seed_y_pred, axis=0)
        seed_row: Dict[str, object] = {"seed": int(seed)}
        for i, h in enumerate(RV_NAMES):
            seed_row[f"rmse_{h}"] = rmse(y_true[:, i], y_pred[:, i])
            seed_row[f"mae_{h}"] = mae(y_true[:, i], y_pred[:, i])
            seed_row[f"spearman_{h}"] = safe_spearman(y_pred[:, i], y_true[:, i])

        fold_diag = aggregate_rows(seed_fold_metrics)
        for metric_name, stats in fold_diag.items():
            if metric_name in seed_row:
                continue
            seed_row[f"{metric_name}_mean_across_folds"] = stats["mean"]
            seed_row[f"{metric_name}_std_across_folds"] = stats["std"]
        per_seed_rows.append(seed_row)

    # Aggregate across seeds using per-seed fold-averaged means
    overall_source_rows: List[Dict[str, float]] = []
    for row in per_seed_rows:
        reduced = {}
        for k, v in row.items():
            if k == "seed":
                continue
            if isinstance(v, (int, float, np.integer, np.floating)) and not k.endswith("_mean_across_folds") and not k.endswith("_std_across_folds"):
                reduced[k] = float(v)
        overall_source_rows.append(reduced)

    overall_metrics = aggregate_rows(overall_source_rows)

    summary = build_summary_json(
        cfg=cfg,
        fold_seed_rows=fold_seed_rows,
        per_seed_rows=per_seed_rows,
        overall_metrics=overall_metrics,
    )

    if cfg.out_dir is not None:
        seed_df = pd.DataFrame(per_seed_rows)
        summary_df = summary_dict_to_frame(overall_metrics)
        seed_df.to_csv(os.path.join(cfg.out_dir, "seed_metrics.csv"), index=False)
        summary_df.to_csv(os.path.join(cfg.out_dir, "summary_table.csv"), index=False)
        write_json(os.path.join(cfg.out_dir, "baseline_metrics_summary.json"), summary)
        manifest = build_run_manifest(
            args=None,
            config=cfg,
            repo_root=Path(__file__).resolve().parents[1],
            device=cfg.device,
            metadata={
                "model_name": cfg.model_name,
                "number_of_folds": int(len(fold_ids)),
                "fold_ids": [int(x) for x in fold_ids],
                "seeds": [int(x) for x in cfg.seeds],
                "output_paths": {
                    "seed_metrics": os.path.join(cfg.out_dir, "seed_metrics.csv"),
                    "summary_table": os.path.join(cfg.out_dir, "summary_table.csv"),
                    "baseline_metrics_summary": os.path.join(cfg.out_dir, "baseline_metrics_summary.json"),
                    "predictions": prediction_dir,
                },
                "elapsed_wall_clock_sec": time.time() - start_time,
                "peak_memory_gb": log_memory(f"after baseline {cfg.model_name}"),
            },
        )
        save_run_manifest(Path(cfg.out_dir) / "run_manifest.json", manifest)

    return summary


