def aggregate_rows(rows: List[Dict[str, float]]) -> Dict[str, Dict[str, float]]:
    if not rows:
        raise ValueError("No rows to aggregate.")

    metric_keys = [k for k in rows[0].keys() if isinstance(rows[0][k], (int, float, np.floating, np.integer))]
    out: Dict[str, Dict[str, float]] = {}

    for key in metric_keys:
        vals = np.asarray([float(r[key]) for r in rows], dtype=np.float64)
        mean_val = float(np.mean(vals))
        std_val = float(np.std(vals, ddof=1)) if vals.size > 1 else 0.0
        out[key] = {
            "mean": mean_val,
            "std": std_val,
            "formatted": format_mean_std(mean_val, std_val),
        }
    return out


