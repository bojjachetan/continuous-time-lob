class DeepLOBDataset(Dataset):
    def __init__(self, npz_path: str):
        data = np.load(npz_path)
        self.X = torch.tensor(data["X"], dtype=torch.float32)           # [N, T, 40]
        self.y = torch.tensor(data["y"], dtype=torch.float32)           # [N, 3]
        self.t_us = data["t_us"].astype(np.int64)
        self.event_id = data["event_id"].astype(np.int64)

    def __len__(self) -> int:
        return int(self.X.shape[0])

    def __getitem__(self, idx: int):
        return {
            "X": self.X[idx],
            "y": self.y[idx],
            "t_us": int(self.t_us[idx]),
            "event_id": int(self.event_id[idx]),
        }


class StaticGCNDataset(Dataset):
    def __init__(self, npz_path: str):
        data = np.load(npz_path)
        self.node_x = torch.tensor(data["node_x"], dtype=torch.float32)  # [N, 20, F_node]
        self.y = torch.tensor(data["y"], dtype=torch.float32)            # [N, 3]
        self.t_us = data["t_us"].astype(np.int64)
        self.event_id = data["event_id"].astype(np.int64)

    def __len__(self) -> int:
        return int(self.node_x.shape[0])

    def __getitem__(self, idx: int):
        return {
            "node_x": self.node_x[idx],
            "y": self.y[idx],
            "t_us": int(self.t_us[idx]),
            "event_id": int(self.event_id[idx]),
        }


