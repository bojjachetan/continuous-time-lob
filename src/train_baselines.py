@dataclass
class RunnerConfig:
    data_dir: str
    baselines_module_path: str

    model_name: str                  # deeplob or static_gcn
    device: str = "cpu"

    batch_size: int = 64
    num_workers: int = 0
    epochs: int = 30
    patience: int = 5
    val_fraction: float = 0.2
    min_val_samples: int = 32
    purge_us: int = 10_000_000

    lr: float = 1e-3
    weight_decay: float = 1e-5
    grad_clip_norm: float = 1.0

    seeds: Tuple[int, ...] = (42, 43, 44)

    # Model kwargs
    deeplob_input_dim: int = 40
    deeplob_conv_channels: int = 32
    deeplob_inception_branch_channels: int = 16
    deeplob_lstm_hidden_dim: int = 64
    deeplob_lstm_layers: int = 1
    deeplob_regression_hidden_dim: int = 128
    deeplob_dropout: float = 0.1

    static_gcn_node_feat_dim: int = 5
    static_gcn_num_levels: int = 10
    static_gcn_hidden_dim: int = 64
    static_gcn_num_layers: int = 3
    static_gcn_dropout: float = 0.1
    static_gcn_regression_hidden_dim: int = 128

    out_dir: Optional[str] = None


def build_model(cfg: RunnerConfig, baselines_module) -> nn.Module:
    if cfg.model_name == "deeplob":
        model = baselines_module.DeepLOBBaseline(
            input_dim=cfg.deeplob_input_dim,
            conv_channels=cfg.deeplob_conv_channels,
            inception_branch_channels=cfg.deeplob_inception_branch_channels,
            lstm_hidden_dim=cfg.deeplob_lstm_hidden_dim,
            lstm_layers=cfg.deeplob_lstm_layers,
            regression_hidden_dim=cfg.deeplob_regression_hidden_dim,
            dropout=cfg.deeplob_dropout,
            out_dim=3,
        )
        return model

    if cfg.model_name == "static_gcn":
        model = baselines_module.StaticGCNBaseline(
            node_feat_dim=cfg.static_gcn_node_feat_dim,
            num_levels=cfg.static_gcn_num_levels,
            hidden_dim=cfg.static_gcn_hidden_dim,
            num_gcn_layers=cfg.static_gcn_num_layers,
            graph_dropout=cfg.static_gcn_dropout,
            regression_hidden_dim=cfg.static_gcn_regression_hidden_dim,
            out_dim=3,
        )
        return model

    raise ValueError(f"Unknown model_name={cfg.model_name}")


def model_forward(
    model: nn.Module,
    batch: Dict[str, Tensor],
    cfg: RunnerConfig,
    edge_index: Optional[Tensor] = None,
) -> Tensor:
    if cfg.model_name == "deeplob":
        return model(batch["X"])
    if cfg.model_name == "static_gcn":
        return model(batch["node_x"], edge_index=edge_index)
    raise ValueError(f"Unknown model_name={cfg.model_name}")


def train_one_epoch(
    model: nn.Module,
    loader: DataLoader,
    optimizer: torch.optim.Optimizer,
    cfg: RunnerConfig,
    device: torch.device,
    edge_index: Optional[Tensor] = None,
) -> float:
    model.train()
    total_loss = 0.0
    total_count = 0

    for batch in loader:
        optimizer.zero_grad()

        batch = {k: v.to(device) for k, v in batch.items()}
        pred = model_forward(model, batch, cfg, edge_index=edge_index)
        loss = F.mse_loss(pred, batch["y"])

        loss.backward()
        torch.nn.utils.clip_grad_norm_(model.parameters(), max_norm=cfg.grad_clip_norm)
        optimizer.step()

        bs = int(batch["y"].shape[0])
        total_loss += float(loss.item()) * bs
        total_count += bs

    return total_loss / max(total_count, 1)


@torch.no_grad()
def evaluate_loss(
    model: nn.Module,
    loader: DataLoader,
    cfg: RunnerConfig,
    device: torch.device,
    edge_index: Optional[Tensor] = None,
) -> float:
    model.eval()
    total_loss = 0.0
    total_count = 0

    for batch in loader:
        batch = {k: v.to(device) for k, v in batch.items()}
        pred = model_forward(model, batch, cfg, edge_index=edge_index)
        loss = F.mse_loss(pred, batch["y"])

        bs = int(batch["y"].shape[0])
        total_loss += float(loss.item()) * bs
        total_count += bs

    return total_loss / max(total_count, 1)


@torch.no_grad()
def evaluate_predictions(
    model: nn.Module,
    loader: DataLoader,
    cfg: RunnerConfig,
    device: torch.device,
    edge_index: Optional[Tensor] = None,
) -> Dict[str, object]:
    model.eval()

    y_true_list = []
    y_pred_list = []
    t_us_list = []
    event_id_list = []

    for batch in loader:
        batch = {k: v.to(device) for k, v in batch.items()}
        pred = model_forward(model, batch, cfg, edge_index=edge_index)

        y_true_list.append(batch["y"].cpu().numpy())
        y_pred_list.append(pred.cpu().numpy())
        t_us_list.append(batch["t_us"].cpu().numpy())
        event_id_list.append(batch["event_id"].cpu().numpy())

    if not y_true_list:
        raise ValueError("Empty evaluation loader.")

    y_true = np.concatenate(y_true_list, axis=0)
    y_pred = np.concatenate(y_pred_list, axis=0)
    t_us = np.concatenate(t_us_list, axis=0).astype(np.int64)
    event_id = np.concatenate(event_id_list, axis=0).astype(np.int64)

    metrics: Dict[str, float] = {}
    for i, h in enumerate(RV_NAMES):
        metrics[f"rmse_{h}"] = rmse(y_true[:, i], y_pred[:, i])
        metrics[f"mae_{h}"] = mae(y_true[:, i], y_pred[:, i])
        metrics[f"spearman_{h}"] = safe_spearman(y_pred[:, i], y_true[:, i])

    return {
        "metrics": metrics,
        "y_true": y_true,
        "y_pred": y_pred,
        "t_us": t_us,
        "event_id": event_id,
    }


# ---------------------------------------------------------------------
# Per-fold execution
# ---------------------------------------------------------------------

def make_dataloaders_for_fold(
    train_path: str,
    test_path: str,
    cfg: RunnerConfig,
) -> Tuple[Dataset, Dataset, Dataset, DataLoader, DataLoader, DataLoader]:
    if cfg.model_name == "deeplob":
        full_train_ds = DeepLOBDataset(train_path)
        test_ds = DeepLOBDataset(test_path)
        train_ds, val_ds = chronological_train_val_split(
            full_train_ds,
            val_fraction=cfg.val_fraction,
            min_val_samples=cfg.min_val_samples,
            purge_us=cfg.purge_us,
        )
        collate_fn = collate_deeplob

    elif cfg.model_name == "static_gcn":
        full_train_ds = StaticGCNDataset(train_path)
        test_ds = StaticGCNDataset(test_path)
        train_ds, val_ds = chronological_train_val_split(
            full_train_ds,
            val_fraction=cfg.val_fraction,
            min_val_samples=cfg.min_val_samples,
            purge_us=cfg.purge_us,
        )
        collate_fn = collate_static_gcn

    else:
        raise ValueError(f"Unknown model_name={cfg.model_name}")

    train_loader = DataLoader(
        train_ds,
        batch_size=cfg.batch_size,
        shuffle=True,
        num_workers=cfg.num_workers,
        collate_fn=collate_fn,
        drop_last=False,
    )
    val_loader = DataLoader(
        val_ds,
        batch_size=cfg.batch_size,
        shuffle=False,
        num_workers=cfg.num_workers,
        collate_fn=collate_fn,
        drop_last=False,
    )
    test_loader = DataLoader(
        test_ds,
        batch_size=cfg.batch_size,
        shuffle=False,
        num_workers=cfg.num_workers,
        collate_fn=collate_fn,
        drop_last=False,
    )

    return train_ds, val_ds, test_ds, train_loader, val_loader, test_loader


def train_one_fold(
    cfg: RunnerConfig,
    baselines_module,
    train_path: str,
    test_path: str,
    seed: int,
) -> Dict[str, object]:
    set_seed(seed)

    device = torch.device(resolve_device(cfg.device))
    model = build_model(cfg, baselines_module).to(device)

    optimizer = torch.optim.AdamW(
        model.parameters(),
        lr=cfg.lr,
        weight_decay=cfg.weight_decay,
    )

    train_ds, val_ds, test_ds, train_loader, val_loader, test_loader = make_dataloaders_for_fold(
        train_path=train_path,
        test_path=test_path,
        cfg=cfg,
    )

    edge_index = None
    if cfg.model_name == "static_gcn":
        edge_np = baselines_module.build_static_lob_edge_index(
            num_levels=cfg.static_gcn_num_levels,
            include_self_loops=False,
            device=device,
        )
        edge_index = edge_np

    best_val = float("inf")
    best_state = None
    best_epoch = -1
    epochs_no_improve = 0

    for epoch in range(1, cfg.epochs + 1):
        _ = train_one_epoch(
            model=model,
            loader=train_loader,
            optimizer=optimizer,
            cfg=cfg,
            device=device,
            edge_index=edge_index,
        )
        val_loss = evaluate_loss(
            model=model,
            loader=val_loader,
            cfg=cfg,
            device=device,
            edge_index=edge_index,
        )

        if val_loss < best_val - 1e-12:
            best_val = val_loss
            best_epoch = epoch
            epochs_no_improve = 0
            best_state = {k: v.detach().cpu().clone() for k, v in model.state_dict().items()}
        else:
            epochs_no_improve += 1

        if epochs_no_improve >= cfg.patience:
            break

    if best_state is None:
        raise RuntimeError("Early stopping failed to capture a best model state.")

    model.load_state_dict(best_state)

    prediction_output = evaluate_predictions(
        model=model,
        loader=test_loader,
        cfg=cfg,
        device=device,
        edge_index=edge_index,
    )
    metrics = dict(prediction_output["metrics"])
    metrics["best_val_loss"] = float(best_val)
    metrics["best_epoch"] = int(best_epoch)
    metrics["num_train_samples"] = int(len(train_ds))
    metrics["num_val_samples"] = int(len(val_ds))
    metrics["num_test_samples"] = int(len(test_ds))
    return {
        "metrics": metrics,
        "predictions": {
            "event_id": prediction_output["event_id"],
            "t_us": prediction_output["t_us"],
            "y_true": prediction_output["y_true"],
            "y_pred": prediction_output["y_pred"],
        },
    }


